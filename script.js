function showProblem(item) {
    const problemDescription = document.getElementById('problem-description');
    problemDescription.innerHTML = `
        <h3>Проблема: ${item.problem}</h3>
        <button onclick="showSolution('${item.solution}')">Как решить проблему?</button>
        <div id="solution" style="display:none; margin-top: 10px;"></div>
    `;
}

function showSolution(solution) {
    const solutionDiv = document.getElementById('solution');
    solutionDiv.innerText = solution;
    solutionDiv.style.display = 'block';
}

function backToSubjects() {
    document.getElementById('topics').style.display = 'none';
    document.getElementById('subjects').style.display = 'block';
}
